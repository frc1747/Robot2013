package org.usfirst.frc.team1747.robot;
/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
	public static final int LEFT_TOP_JAG_ID=1;
	public static final int LEFT_MID_JAG_ID=3;
	public static final int LEFT_BOT_JAG_ID=5;

	public static final int RIGHT_TOP_JAG_ID=2;
	public static final int RIGHT_MID_JAG_ID=4;
	public static final int RIGHT_BOT_JAG_ID=6;
	
	public static final int SHOOTER_JAG_ID=7;
	public static final int FEEDER_JAG_ID=0;
}
